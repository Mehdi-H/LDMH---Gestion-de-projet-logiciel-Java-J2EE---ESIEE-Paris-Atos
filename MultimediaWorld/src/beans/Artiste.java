package beans;

public class Artiste 
{
	// ========================================================================
	// == CHAMPS
	// ========================================================================
	
	private int id;
	private String nom;
	
	// ========================================================================
	// == GETTERS & SETTERS
	// ========================================================================
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
}
